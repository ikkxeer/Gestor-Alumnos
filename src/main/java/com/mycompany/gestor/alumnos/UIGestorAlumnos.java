    private void buttonInsertarAlumnoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonInsertarAlumnoActionPerformed
        // Si hay algun campo vacio mostramos mensaje
        if (txtAreaNombre.getText().isEmpty() || txtAreaPrimerApellido.getText().isEmpty() 
                || txtAreaSegundoApellido.getText().isEmpty() || txtAreaFecha.getText().isEmpty() 
                || txtAreaClasse.getText().isEmpty()) {
            
            JOptionPane.showMessageDialog(this, "Rellena todos los campos!");
        } 
        // Si la fecha no es valida mostramos mensaje
        else if(!validarFecha(txtAreaFecha.getText())) {
            JOptionPane.showMessageDialog(this, "Formato de fecha no valida! \nEjemplo de formato: 1988/11/10");
        } 
        // Insertamos el alumno con los contenidos
        else {
            String nombre = txtAreaNombre.getText();
            String primerApellido = txtAreaPrimerApellido.getText();
            String segundoApellido = txtAreaSegundoApellido.getText();
            String fecha = txtAreaFecha.getText();
            String classe = txtAreaClasse.getText();
            
            AlumnoDTO alumnoAInsertar = new AlumnoDTO(nombre, primerApellido, segundoApellido, fecha, classe);
            
            gestorAlumnos.InsertarAlumno(alumnoAInsertar);
            JOptionPane.showMessageDialog(this, "Se ha insertado el alumno en la base de datos!");
        }
    }//GEN-LAST:event_buttonInsertarAlumnoActionPerformed

    private void buttonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonEliminarActionPerformed
        // Obtenemos el alumno seleccionado
        AlumnoDTO alumnoSeleccionado = listAlumnos.getSelectedValue();
    
        // Si no hay ningun alumno seleccionado
        if(alumnoSeleccionado == null) {
            JOptionPane.showMessageDialog(this, "Selecciona un alumno");
            return;
        }
        
        // Cuadro para confirmar la eliminacion de un alumno
        int confirmacion = JOptionPane.showConfirmDialog(
            this,
            "¿Eliminar a " + alumnoSeleccionado.getNombre() + "?",
            "Confirmar eliminación",
            JOptionPane.YES_NO_OPTION
        );

        
        if(confirmacion == JOptionPane.YES_OPTION) {
            // Eliminamos al alumno
            gestorAlumnos.EliminarAlumno(alumnoSeleccionado.getId());
            // Cargamos la lista de nuevo
            cargarAlumnos();
            JOptionPane.showMessageDialog(this, "Alumno eliminado correctamente");
        }        
    }//GEN-LAST:event_buttonEliminarActionPerformed

